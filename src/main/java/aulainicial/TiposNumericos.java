public class TiposNumericos {
    public static void main(String[] args) {
        Byte numeroByte = 127; //-128 ate 127
        Short numeroShort = 1; //-32768 ate 32767
        Integer numeroInteger = 2147483647; //-2147483648 ate 2147483647
        Long numeroLong = 1000000000000000000L; //-9223372036854775808 ate 9223372036854775807
        Float numeroFloat = 3.14f; //-3.4028235E38 ate 3.4028235E38
        Double numeroDouble = 3.14; //-1.7976931348623157E308 ate 1.7976931348623157E308



    }

}
